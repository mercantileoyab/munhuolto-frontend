function ColorlibStepIcon(props) {
  const { active, completed, className } = props;

  const icons = {
    1: <SettingsIcon />,
    2: <GroupAddIcon />,
    3: <VideoLabelIcon />,
  };

  return (
    <ColorlibStepIconRoot ownerState={{ completed, active }} className={className}>
      {icons[String(props.icon)]}
    </ColorlibStepIconRoot>
  );
}

ColorlibStepIcon.propTypes = {
  /**
   * Whether this step is active.
   * @default false
   */
  active: PropTypes.bool,
  className: PropTypes.string,
  /**
   * Mark the step as completed. Is passed to child components.
   * @default false
   */
  completed: PropTypes.bool,
  /**
   * The label displayed in the step icon.
   */
  icon: PropTypes.node,
};

// const steps = ['Päivä ja auto', 'Yhteystiedot', 'Varaa ja vahvista'];

export default function CustomStepper() {

    const location = useLocation();
    const { t } = useTranslation();
    const steps = [
        t("customStepper.step1"),
        t("customStepper.step2"),
        t("customStepper.step3")
    ];

    const [activeStep, setActiveStep] = React.useState(0);

    React.useEffect(() => {
        if (location.pathname.startsWith("/reservations")) {
            setActiveStep(5);
        } else {
            const params = new URLSearchParams(location.search);
            setActiveStep(Number(params.get('step')) - 1);
        }
    }, [location]);

    return (
        <Grid container spacing={1} className="custom-stepper-container">
          <StepperArrowLeft />
          <Grid item size={{xs: 10, sm: 10, md: 10, lg: 10}} className="custom-stepper">
              <Stepper alternativeLabel activeStep={activeStep} connector={<QontoConnector />}>
                  {steps.map((label) => (
                  <Step 
                    key={label}
                    
                  >
                      <StepLabel StepIconComponent={QontoStepIcon}>{label}</StepLabel>
                  </Step>
                  ))}
              </Stepper>
          </Grid>
          <StepperArrowRight />
        </Grid>
    );
}